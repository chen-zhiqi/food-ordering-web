
public class Review {
	
	protected String review;

	/**
	 * @return the review
	 */
	public String getReview() {
		return review;
	}

	/**
	 * @param review the review to set
	 */
	public void setReview(String review) {
		this.review = review;
	}

	public Review(String review) {
		super();
		this.review = review;
	}

}
